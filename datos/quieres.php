Desarrolladores web.

Diseñadores web.

Diseñadores gráficos.

*Administradores

Los Administradores realizaran la función de moderar Kimvex mantener libre spam, y eliminar todo tipos de propaganda ofensiva a los demás usuarios.

Personas con buena ortografía para desempeñar el servicio del foro de Kimvex, postear las nuevas actualizaciones, cualquier tipo de información. 

Personas con actitud de paciencia para ayudar a los usuarios de Kimvex en algún problema con sus cuentas o que pidan información de cualquier tipo relacionado a Kimvex.

Al ser voluntario no estarás con un horario fijo que te mantenga y exija varias horas de trabajo continuo, podrás desempeñar tu servicio de voluntario en el momento que tengas libre en el día.

Lo que Kimvex te pide es que puedas ayudar en todo momento con tu disponibilidad si es posible, y conectarte por lo menos una hora al día.

