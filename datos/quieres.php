Bienvenido a Kimvex, quieres una respuesta a esta pregunta: ¿Quieres ser parte de Kimvex? Kimvex es una comunidad que está en crecimiento y debido a ello necesitamos tu ayuda. Si eres estudiante de universidad y quieres poner en práctica lo que has aprendido durante tu tiempo de estudio en ella Kimvex te da la oportunidad de hacerlo, no esperes hasta salir de la universidad para poder hacer lo que te gusta y mostrarle al mundo tu capacidad, además de tomar experiencia para trabajar en el futuro.

