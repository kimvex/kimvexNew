Lo importante.

Kimvex es un servicio sin fines de lucro por tal motivo Kimvex no puede dar salario a las personas que quieran entrar a Kimvex y ser parte de él. Por ese motivo las personas que quieran participar en Kimvex lo harán de la forma de voluntarios, prestando sus servicios sin esperar salario a cambio, más lo que Kimvex les puede asegurar es que tomaran más experiencia en la especialidad en la que quieras ayudar. 

