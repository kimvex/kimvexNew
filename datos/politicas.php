			<h1 class="texoGeneralTitulo">Políticas de privacidad</h1>
			<p class="textoGeneral">

Versión: 1.0

Con kimvex puedes compartir distinta información para estar en contacto con las personas que conoces. Cuando compartes datos con kimvex en este caso al crear una cuenta, podemos recoger distinta información para mejorar su interacción en kimvex de esta manera te damos a conocer como utilizamos tus datos y la forma en que puedes proteger tu privacidad.

Estos son los puntos que encontraras en estas políticas de privacidad:

Cuales datos recogemos y cuáles son los fines para los cuales serán utilizados.

Como y quienes pueden acceder a ellos.

Como puedes mantener protegido tus datos.

Que información recogemos y como la utilizamos:

Cuando te registras en kimvex proporcionas una serie de datos las cuales son nombre, apellido y sexo. Con esta información puedes ser reconocido dentro de kimvex.

Credencial:

Una vez creada tu cuenta kimvex eres libre de proporcionar más información en la sección credencial en la que se recolectan distintos datos los cuales solo serán visibles para tus amigos. Si colocaste tus datos estos serán guardados y utilizados por kimvex para mantener un control aún más amplio de su cuenta en cuanto a gustos en distintos tipos de aspectos. Y además darás a conocer más sobre ti a tus amigos.

Uso amplio de la credencial:

Una vez que has actualizado tu información kimvex podrá hacer uso de tu información para el control de prioridades de tu cuenta, nos reservamos el derecho a usar tus datos en momentos en que las autoridades requieran de dicha información, mas no utilizaremos tu información para proporcionarlas a terceros. 

Publicaciones:

Las publicaciones que realices en tu cuaderno serán vistos únicamente por usuarios a los cuales has aceptado como amigo o has enviado solicitud y en el momento en que te acepta, además de ser visible para personal autorizado de kimvex, las publicaciones hechas en el cuaderno global son visibles para todos los usuarios de kimvex.

Fotografías e imágenes:
Tus fotografías e imágenes que subas a tu galería son visibles solo a tus amigos y a personal autorizado de kimvex.

Videos:
Los videos que subas a tu galería de videos serán visibles para tus amigos y personal autorizado kimvex.

Portafolio:
Los distintos archivos que subas a tu portafolio serán únicamente vistos por amigos y personal autorizado de kimvex.

Eliminación de cuenta:
Tienes la opción de eliminar tu cuenta en el momento que desees.

1.1 datos eliminados:
Los datos que se eliminaran serán, fotos e imágenes, archivos del portafolio, videos. 

1.2 datos no eliminados:
Cuando eliminas tu cuenta kimvex hay datos e información que no se elimina en la acción en este caso tus publicaciones hechas en el cuaderno global, en el cuaderno de tus amigos, mensajes enviados a tus amigos y mensajes recibidos, datos de la credencial.

1.3 Como eliminar la información restante:
Para poder eliminar cierta información deberás seguir los siguientes pasos:

*Los datos que no se eliminaran serán mensajes enviados a otros usuarios, publicaciones hechas en el cuaderno global, publicaciones hechas en el cuaderno de tus amigos.

*datos que si se eliminaran serán mensajes recibidos y datos de la credencial.

Para poder eliminarlos antes de eliminar tu cuenta enviar un mensaje a la administración de kimvex y de esta forma tus mensajes recibidos y datos personales de la credencial serán eliminados.

Protección de sus datos:
Los datos proporcionados y publicaciones dentro de su perfil al igual que fotos e imágenes, videos y archivos diversos serán visibles únicamente a sus amigos dentro de kimvex. De esta manera kimvex le exhorta a que tengas conocimientos de quienes aceptas como amigos, de esta manera tendrás controlado tu perfil en privacidad más estable.

Al estar registrado en kimvex es responsable de su información personal y a quienes da su consentimiento de observarla. 

Cambios de esta Política de privacidad.
Kimvex se reserva los derechos a modificar esta política de privacidad en el momento que lo crea conveniente y están sujetas dentro de los términos y condiciones.
