Versión: 1.0

Estos términos y condiciones del servicio que se muestran en los siguientes puntos son los que manejan la interacción de kimvex y sus usuarios al igual de los usuarios a otros usuarios. Al registrarte en kimvex has aceptado dichos términos y condiciones y está de acuerdo con que kimvex pueda modificarlos en el momento que lo crea oportuno.

Condiciones básicas.
Al usar el servicio de kimvex dentro del mismo, queda como responsable del uso que usted pueda darle, el contenido que usted suba a kimvex (imágenes,fotografias,archivos,videos) o el tipo de información que usted proporcione.

1.1.- Archivos de video.

Usted no debe subir archivos de videos con contenido pornográfico, violencia extrema, racismo, maltrato animal, respetando la sensibilidad de los demás usuarios. Al incumplir este punto su video será eliminado inmediatamente.

1.1.2.- Demás archivos de video.

Usted es libre de subir cualquier video con contenido propio que cumplan los términos del punto 1.1, si usted sube a su página de kimvex algún video con contenido que no sea de su propiedad usted será responsable de cualquier repercusión que el conlleve deslindando absolutamente a kimvex de tal.

1.1.3.- Propiedad de video

Los videos que usted suba a kimvex son de su propiedad siempre y cuando no infrinjan los derechos de autor, kimvex no es dueño de su contenido por tal motivo usted es el responsable del contenido del tal video y de la privacidad del mismo este término se encuentra en la parte de amigos.

1.2.- Archivo de imagen.
Usted no debe subir archivos de imagen y fotografías con contenido pornográfico, violencia extrema, racismo, maltrato animal, respetando la sensibilidad de los demás usuarios. Al incumplir este punto la imagen y/o fotografía será eliminada inmediatamente.

1.2.1.- Usted es libre de subir a su galería de kimvex cualquier imagen y/o foto en formato .jpg, .gif, .png, etc... Siempre y cuando cumplan con el punto 1.2.

1.2.3.- Propiedad de imagen y/o fotografía.
Usted es el dueño de los archivos de imágenes y/o fotografías que suba a kimvex siempre y cuando no infrinjan los derechos de autor y por tal motivo usted es responsable de las imágenes y/o fotografías y de cualquier repercusión que ella conlleve deslindando absolutamente a kimvex, sus directores, voluntarios de tal.

1.3.- Publicaciones.

Usted es libre de publicar sobre cualquier temática que le apetezca siempre y cuando no aludan a motivos de racismo, amenazas e insulto a demás miembros dentro de kimvex o persona fuera de tal. 

Usted es responsable de lo que publica por tal motivo le exhortamos a que piense bien antes de hacer una publicación, en caso de publicar algo indebido elimínelo o afronte las repercusiones impuestas por autoridades si ese fuera el caso deslindando a kimvex, sus directores, voluntarios de cualquier responsabilidad por su publicación.

1.4.- Mensajes privados.
Usted es libre de enviar mensajes a cualquier usuario de kimvex, pero queda estrictamente prohibido el insultar u ofender a otro usuario, de igual manera queda estrictamente prohibido el acosar a otro usuario de kimvex y tomando en cuenta que el acoso es un delito penado por la ley. 

2.- Privacidad.

Usted es responsable de la privacidad de su cuenta en los siguientes puntos:
Contraseña
Debe mantener su contraseña actualizada. Kimvex le recomienda cambiar su contraseña máximo cada 3 meses usando números, letras entre mayúsculas y minúsculas, símbolos. 
No de su contraseña a otras personas para mantener mayor seguridad en su cuenta, si alguien ha robado su cuenta kimvex infórmenos y aremos lo posible por recuperar su cuenta.

2.-Amigos.

Sus datos, archivos, videos, imágenes, publicaciones son solamente visibles para sus amigos, por tal motivo le recomendamos que cuando agregue a un amigo o acepte su solicitud de amistad lo conozca de esta manera protegerá mejor de sus datos personales.

3.- Conducta dentro de kimvex.
3.1.- Queda prohibido insultar, ofender, acosar a otros usuarios de kimvex y/o fuera de kimvex por medio de videos, imágenes, fotografías y publicaciones dentro de kimvex. 
El tipo de conducta inapropiada será sancionada dentro de kimvex con la suspensión y posterior eliminación de su cuenta sin previo aviso por parte de kimvex.

3.2.-Puedes usar kimvex para expresarte sobre muchos temas o solo para mantenerte comunicado con tus amigos y/o conocidos.

3.3.-No puedes.

3.3.1.-Fomentar por medio de publicaciones, imágenes, fotografías y videos actividades de tipo violenta, pornográfica, fanatismo, intolerancia, racismo, agresión a menores, conducta auto lesiva (que fomenten a otros usuarios a cortarse, mutilarse, inciten al suicidio, inciten a la bulimia, anorexia u otros trastornos de la alimentación).

3.3.2.-Publicar imágenes, fotos, videos, publicaciones con contenido para escandalizar a los demás usuarios.

3.3.4.-No hagas spam en publicaciones, mensajes privados y chat.

3.3.5.-No coloques enlaces engañosos en tus publicaciones. (No andes de mentiroso).

3.3.6.-No suplantar la identidad de otra persona.

3.3.7.-No publiques información (personal) sobre otros usuarios o personas fuera de kimvex (difamándola, sin importar si fue tu novio/a y te dejo por irse con otra persona).

3.3.8.-Esta estrictamente prohibido aprovecharse de indebidamente de cualquier fallo en el servicio de kimvex.

3.3.9.-Esta estrictamente prohibido publicar videos, fotos, imágenes, y publicaciones sobre maltrato animal, o que fomenten al maltrato animal. Ya que los directivos de kimvex amamos a los animales.

4.- Terminación y eliminación:
4.1.-A la terminación o a eliminación de su cuenta algunos datos, imágenes, fotografías, comentarios pueden no ser eliminados de inmediato y podrían permanecer por un tiempo razonable, en el caso de publicaciones con imágenes y/o fotografías hechas en algún comentario o publicación hecha fuera de su perfil.

5.-Kimvex dice:
El incumplir con cualquiera de las normas establecidas en los anteriores puntos puede que reciba un mensaje en su cuenta por parte de administradores en su cuaderno o por parte de los súper administradores en sus mensajes privados. Si no explica el porqué de su comportamiento y/o corrige su comportamiento su cuenta será suspendida o eliminada. En cualquier caso de los anteriores kimvex se reserva el derecho de eliminar o suspender cualquier cuenta sin previo aviso.

De igual manera kimvex se reserva el derecho a modificar sus términos y condiciones con o sin previo aviso, por este motivo les recomendamos estar atentos a este documento periódicamente.

